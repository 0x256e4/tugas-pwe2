    <h1>Resep Nasi Goreng</h1>
    <h2>Bahan-Bahan</h2>
    <ul>
        <li>1 potong daging paha ayam, rebus, suwir</li>
        <li>2 butir telur, kocok lepas</li>
        <li>150 g udang tanpa kulit</li>
        <li>5 buah bakso sapi, iris</li>
        <li>5 buah bakso ikan, iris</li>
        <li>500 g nasi putih</li>
        <li>2 sdm Bango Kecap Manis</li>
        <li>1 sdt garam</li>
        <li>Â½ sdt gula pasir</li>
        <li>1 batang daun bawang, iris tipis</li>
        <li>1.5 sdm bawang merah goreng</li>
        <li>3 sdm minyak, untuk menumis</li>
        <li>1 sdt terasi, bakar</li>
        <li>3 buah cabai merah besar</li>
        <li>2 siung bawang putih</li>
        <li>6 butir bawang merah</li>
    </ul>
    <h2>Cara pembuatan</h2>
    <ol>
        <li>Panaskan minyak. Tumis bumbu tumbuk kasar sampai harum. Sisihkan di pinggir wajan. Masukkan telur. Aduk sampai berbutir.</li>
        <li>Tambahkan ayam, udang, bakso. Aduk rata. Tambahkan nasi putih. Aduk- aduk. Masukkan Bango Kecap Manis, garam, dan gula pasir. Aduk sampai matang.</li>
        <li>Masukkan daun bawang. Aduk rata. Sajikan bersama telur ceplok dan taburan bawang merah goreng.</li>
    </ol>
