    <h1>Resep Pempek</h1>
    <h2>Bahan-Bahan</h2>
    <ul>
        <li><h3>Pempek :</h3></li>
        <ul type="circle">
            <li>250 gram tepung tapioka</li>
            <li>125 gram tepung terigu</li>
            <li>250 ml air</li>
            <li>2 sdt garam</li>
            <li>1 sdt kaldu bubuk</li>
            <li>3 siung bawang putih, haluskan</li>
            <li>100 gram teri nasi asin (cuci bersih, lalu blender)</li>
            <li>2 butir Telur</li>
            <li>Secukupnya telor puyuh untuk isian pempek</li>
        </ul>
        <li><h3>Bahan Kuah Cuko :</h3></li>
        <ul type="circle">
            <li>250 gram Gula merah</li>
            <li>500 ml air</li>
            <li>2 sdm Asam jawa</li>
            <li>1 sdt cuka masak</li>
            <li>1 sdt cuka masak</li>
        </ul>
        <li><h3>Bumbu halus Kuah Cuko :</h3></li>
        <ul type="circle">
            <li>8 siung Bawang putih</li>
            <li>5 buah Cabe merah keriting</li>
            <li>10 buah Cabe rawit merah</li>
            <li>2 sdm Ebi sangrai</li>
            <li>1 sdt Garam</li>
        </ul>
    </ul>
    <h2>Cara pembuatan</h2>
        <ul>
            <li>Pempek :</li>
            <ol style="text-align: justify;">
                <li>Bikin adonan: Rebus air sampai mendidih, masukkan garam, kaldu bubuk, bawang putih yang sudah dihaluskan dan teri nasi yang sudah dihaluskan (diblender), kecilkan api lalu masukkan terigu sedikit demi sedikit sambil diaduk cepat dengan spatula kayu hingga adonan menggumpal, matikan api dan biarkan hangat.</li>
                <li>Pindahkan adonan: Pindahkan adonan tadi ke mangkuk besar, tambah telur satu per satu sambil dimixer hingga lembut.</li>
                <li>Masukkan bahan lain dalam adonan: Setelah telur, masukkan juga tepung tapioka sedikit demi sedikit sambil diuleni pakai tangan sampai tercampur rata (nguleninnya pelan aja) jika lengket tidak masalah.</li>
                <li>Rebus pempek: Didihkan air yang diberi sedikit minyak goreng. Sementara itu, cetak pempek. Ambil secukupnya adonan, bentuk agak memanjang lalu tekan sedemikian rupa hingga membentuk lubang yang dalam, isi dengan 1 buah telor puyuh, lalu rekatkan bagian atasnya, pastikan adonan tidak bocor. Masukkan ke air mendidih dan rebus sampai mengapung dan matang. Angkat, tiriskan, dinginkan dan siap digoreng.</li>
            </ol>
            <br>
            <li>Kuah Cukko :</li>
            <ol style="text-align: justify;">
                <li>Campurkan semua bahan dan bumbu lalu masak sampai mendidih dan gula larut sambil sesekali diaduk, test rasa.</li>
                <li>Matikan api, lalu saring. Saat hangat, tambahkan cuka aduk rata. Biarkan cuko dingin.</li>
                <li>Sajikan pempek goreng dengan kuah cuko dan pelengkapnya seperti irisan timun.</li>
            </ol>
        </ul>
